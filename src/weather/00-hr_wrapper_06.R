# Wrapper to run high resolution data fetch & map generation.
setwd("/home/snowreports/R/weather/")
cat("* Fetching HR data for 06Z forecast.")
sink("/dev/null")
system("export TZ=UTC")
source("01-get_hr_data_06.R")
sink(type="output")
cat("* Generating HR forecast map.")
sink("/dev/null")
gc()
source("02-map_hr_data.R")
rm(list=ls())
gc()
sink(type="output")
cat("* Map local forecasts.")
sink("/dev/null")
source("05-get_hr_local.R")
system("export TZ=America/Vancouver")
sink(type="output")
cat("* Done.")
